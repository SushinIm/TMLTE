package com.TM.LTE.bean;

public class ReserveTicket {
	private int rt_num;
	private int rt_tnum;
	private String rt_mnid;
	private int rt_qty;
	private String rt_date;
	private int rt_total_price;
	private int rt_use;
	public int getRt_num() {
		return rt_num;
	}
	public void setRt_num(int rt_num) {
		this.rt_num = rt_num;
	}
	public int getRt_tnum() {
		return rt_tnum;
	}
	public void setRt_tnum(int rt_tnum) {
		this.rt_tnum = rt_tnum;
	}
	public String getRt_mnid() {
		return rt_mnid;
	}
	public void setRt_mnid(String rt_mnid) {
		this.rt_mnid = rt_mnid;
	}
	public int getRt_qty() {
		return rt_qty;
	}
	public void setRt_qty(int rt_qty) {
		this.rt_qty = rt_qty;
	}
	public String getRt_date() {
		return rt_date;
	}
	public void setRt_date(String rt_date) {
		this.rt_date = rt_date;
	}
	public int getRt_total_price() {
		return rt_total_price;
	}
	public void setRt_total_price(int rt_total_price) {
		this.rt_total_price = rt_total_price;
	}
	public int getRt_use() {
		return rt_use;
	}
	public void setRt_use(int rt_use) {
		this.rt_use = rt_use;
	}
}
